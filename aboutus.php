                                The spark for the idea of Way for Life came about after three like-minded classmates were exposed and eventually astonished by the massive discrepancy that existed between an underprivileged student who studied in a Government school and a student in a Private school. The obvious financial imbalance that both of the students came from, coupled with the lack (almost an absolute absence) of even the most necessary infrastructure in the Government schools greatly affected the comfort and naivety of our founders. This inequality, they resolved, was one that required immediate correction.
                                Way for Life started off in 2017 with 3 members  and the support of a few local retailers in Bangalore. Witnessing a steady growth of support and participation from onlookers soon after the very 1st event instilled in the members then not only the confidence to proceed with their actions, but also a conviction that what they were putting their collective efforts into would soon blossom into fruition.
